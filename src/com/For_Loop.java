package com;

public class For_Loop {

	public static void main(String[] args) {
		
		int b;
		for (b=0;b<=8;b++)
		{
			System.out.println("*");
			System.out.println("**");
		}
		
		//-------------------------------------------------------------
		
		int a;
		for ( a=0 ; a<=19 ; a++ )
		{
			System.out.println("* |-- < R R > --| *");
		}
	//-----------------------------------------------------------------------	
		int i;                               //------------------12345
		for (i=1;i<=5;i++)
		{
			System.out.println(i);
		}
	//------------------------------------------------------------------------	
		int j;                               //------------------54321
		for (j=5;j>=1;j--)
		{
			System.out.println(j);
		}
	//--------------------------------------------------------------------------	
		int n;                               //------------------246810
		for (n=0;n<=10;n+=2)
		{
			System.out.println(n);
		}
	//--------------------------------------------------------------------------	
		int k;                               //-------------------108642
		for (k=10;k>=0;k-=2)
		{
			System.out.println(k);
		}
	//------------------------------------------------------------------------------	
		int l;                              //----------------------13579
		for (l=1;l<=10;l+=2)
		{
			System.out.println(l);
		}
	//-------------------------------------------------------------------------------	
		int m;                             //------------------------97531
		for (m=9;m>=0;m-=2)
		{
			System.out.println(m);
		}
	//----------------------------------------------------------------------------------	
	}
}
